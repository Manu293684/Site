                        <h2>Teatru</h2>
                        <h4 class="posted">de <a class="admin" href="#">Alex Vrinceanu</a> / <a class="date" href="#">Mai 05</a>, <a class="year" href="#">2020</a></h4>
                        <p>Voi știți ce este Semeringul Bănățean? Până să ajungem în Cheile Nerei, noi nu auzisem vreodată această denumire. Dar am aflat: este supranumele dat celei mai vechi căi ferate montane din România, ce face legătura dintre Oravița și Anina (județul Caraș-Severin). Este a cincea cale ferată construită pe actualul teritoriu al țării noastre și cu siguranță una dintre cele mai frumoase.</p><h2><b>Ce face ca această cale ferată să fie deosebită?</b></h2><p>Primul lucru care ne-a șocat a fost că toate tunelele de pe traseu au fost săpate ”manual”! Și asta pentru că proiectul a fost inaugurat în 1863, cu 3 ani înainte de inventarea dinamitei. Tocmai de aceea tunelele sunt foarte înguste, suficient doar cât să încapă trenul și vreo palmă în plus. Ce s-or mai fi căznit oamenii să le sape!</p>
						<p>Din păcate noi am plecat pe frig și ploaie din Oravița și am ajuns pe zăpadă în Anina. Deși teoretic era primăvară. Oricum ne-am bucurat de peisaj, care era înverzit deja!</p>
						<p>Cât despre teatrul din Oravița, pentru noi a fost o surpriză foarte plăcută! Am aflat cu stupoare că este cel mai vechi teatru din România. La Oravita! Serios?! Cine ar fi crezut?</p>
						<p>De afară pare o clădire ca oricare alta. Nu aveam așteptări când am ajuns. Dar am intrat și ne-am minunat ca niște copii. Am rămas cu gura căscată când am văzut atâta eleganță într-un loc atât de sumbru.</p>
						<p>Sunt 15 tunele în total, în cei 34 de km ai traseului. Iar peisajul este foarte frumos, pe alocuri spectaculos. Dar vă lăsăm să vedeți cu ochii voștri:</p>
