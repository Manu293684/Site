								<p>Calea ferată Oravița–Lișava–Anina a fost inaugurată la 15 decembrie 1863, fiind prima cale ferată montană (și a patra construită) de pe teritoriul actual al României. Construcția ei a început în anul 1861 și a fost o lucrare inginerească dificilă pentru că se situa într-o regiune muntoasă (partea de vest a Munților Banatului), greu accesibilă urcând pe cei 33,4 km ai traseului, 339 de m, având nu mai puțin de 14 tunele cu o lungime totală de 2,1 km și 9 viaducte.</p>
