                        <h2>Stil de viață</h2>
                        <h4 class="posted">de <a class="admin" href="#">Irina Gabor</a> / <a class="date" href="#">August 7</a>, <a class="year" href="#">2020</a></h4>
                        <p>Nu încape discuție și tindem a judeca că nu-i nimeni în sala care să se opună: călătoriile sunt cele mai grozave lucruri pe care le poți face pentru a te înfrunta cu noul, a-ți schimba perspectivele și a-ți lărgi la infinit orizonturile.</p> <p>Noi, la momondo, o spunem și am spus-o mereu: călătoriile ne deschid mințile și ne fac să vedem diferențele dintre noi ca pe surse de inspirație și dezvoltare, și nu ca motive de intoleranță și prejudicii. Credem că oricine trebuie să aibă dreptul să călătorească, indiferent de vârstă, gen sau situație financiară.</p>
                        <p>În același timp, suntem conștienți de un lucru: călătoriile sunt treburi personale și înseamnă ceva diferit pentru fiecare dintre noi; de aceea am ținut musai să intrăm în contact cu câțiva dintre cei care au microbul călătoriilor adânc afundat în sânge și să le ascultăm gândurile referitoare la asta. </p>
						
                        <br><br>
                        <div class="blog-one-quote">
                            <p><span><i class="fa fa-angle-double-right"></i></span> Călătoriile ne ajută să ne lărgim aria de cunoaștere și să socializăm cu diferite tipuri de oameni. De exemplu, am călătorit în multe țări ale lumii, însă fiecare călătorie a fost unică, din fiecare am rămas cu amintiri plăcute și am legat amiciții cu fel și fel de oameni. </p>
                            <p><span><i class="fa fa-angle-double-right"></i></span>Călătoriile sunt un mod relaxant de a ne petrece timpul liber alături de persoanele dragi nouă.</p>
                            <p><span><i class="fa fa-angle-double-right"></i></span>Călătoriile au fost, sunt și vor fi cel mai frumos mod de a ne relaxa și de a ne îmbogăți cultura. </p>
                        </div><br><br>
                        <p>Calatoriile sunt o modalitate foarte incantatoare de petrecere a timpului liber, reprezinta o experienta ce poate sa imbunatateasca calitatea vietii. Ne permit sa petrecem mai mult timp cu noi insine, ne fac mai toleranti si mai sensibili in relatiile cu ceilalti, intrucat invatam sa intalnim si sa cunoastem tot felul de oameni. Efectiv ne invata sa traim viata la maximum, de aceea calatoriile sunt foarte importante in viata unui om.</p>
