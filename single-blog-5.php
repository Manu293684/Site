                        <h2>Să înceapă aventura</h2>
                        <h4 class="posted">de <a class="admin" href="#">Claudiu Vela</a> / <a class="date" href="#">Iunie 26</a>, <a class="year" href="#">2020</a></h4>
                        <p>Pe langa a inspira, orice jurnal, orice scriere si o fotografie mai are si un rol de a ingheta cumva in timp momente care peste ani si ani ar deveni pierdute in negura memoriei. Astfel incat totul e un amestec intre a scrie sau a face fotografii pentru tine, si a scrie sau a face fotografii cu gandul ca vor fi citite sau vazute si de altii.</p>
                        <p><b>Tunelul iubirii</b> : Atunci când vezi pentru prima oară fotografiile te întrebi dacă este într-adevăr un loc real sau doar rezultatul muncii unei persoane cu real talent în prelucrarea imaginilor. Descoperit întâmplător de către doi fotografi amatori, Tunelul Iubirii a fost recent introdus pe lista comorilor ascunse din Europa, iar motivele sunt lesne de înțeles. Pe calea ferată care leagă localitățile Obreja și Glimboca, în județul Caraș-Severin, se află un loc misterios, o fostă cale ferată abandonată, unde natura este acum stăpână. Parlamentul European a inclus-o pe lista sa cu 28 de locuri de văzut în Europa, pe locul 19, spunând despre acest loc că este cel mai romantic din România și poate și din lume.</p>
                        <p>Pentru cei care si-au propus vizitarea Cascadei Bigar, le recomand si vizitarea Morilor de apa de la Rudaria (Eftimie Murgu) care se afla in drumul spre Bozovici la o distanta aproximativa pana in 20 de km de la Cascada Bigar. Aria naturala se afla în Munții Almăjului (grupare montană a Munților Banatului), la o altitudine cuprinsă între 350 și 900 m, pe valea Rudăriei (pe a cărui curs sunt amplasate vechi mori de apă), în sudul județului Caraș-Severin, pe teritoriul administrativ al comunei Eftimie Murgu.</p>
                        <p>Ieșirile pe munte sunt superbe, dau dependenţă! Dacă mergi pentru prima dată pe munte, este bine să afli acum toate informaţiile esenţiale ce vor face din aventura ta una plăcută. Aici venim noi în ajutor cu toată experienţa noastră, după ce am ajutat mii de oameni (unii, novici în mersul pe munte) iar acum te ajutăm și pe tine cu cele mai utile sfaturi. </p>
                        <div class="blog-one-quote">
                            <p><span><i class="fa fa-angle-double-right"></i></span><b>Împachetezi tot ce trebuie:</b> Pelerină de ploaie (indiferent de vreme), frontală (indiferent de durata excursiei), baterii de rezervă, tricouri tehnice (minim unul de rezerva in rucsac), cremă de soare, șapcă/bandană, haine potrivite vremii. Nu e arta sa asezi lucrurile in rucsac, dar e important sa respecti cativa pasi.  Cum iti aranjezi hainele in rucsac. Într-o tură de 5–8 ore, ia cu tine pe munte: 1.5–2L de apă, gustări ușoare (nuci, batoane, biscuiţi, mere), șapcă, cremă de soare, tricou + șosete de schimb, frontală, pelerină de ploaie. Nu pune nimic ce crezi că nu ai folosi. Orice gram, după o oră de traseu, devine un kilogram!</p>
                            <p><span><i class="fa fa-angle-double-right"></i></span><b>Testezi bocancii: </b>Fii atent(ă) să fie măsura potrivită – un pic mai mari decât piciorul. Încearcă-i o zi – două neapărat, mergi în ei prin oraș până te convingi că nu îţi vor provoca niciun disconfort pe munte.</p>
                            <p><span><i class="fa fa-angle-double-right"></i></span><b>Testezi echipamentul:</b>Cort, beţe, aparat foto – tot ce e nou trebuie testat. Vrei să faci poze superbe și să dormi bine? Fii prevăzător!</p>
