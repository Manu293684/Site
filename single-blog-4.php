                        <h2>Explorarea naturii </h2>
                        <h4 class="posted">de <a class="admin" href="#">Cosmin Fion</a> / <a class="date" href="#">Iulie 01</a>, <a class="year" href="#">2020</a></h4>
                        <p>Prima voastră întâlnire cu Natura. Prima întâlnire cu Natura a copiilor voștri. Vi le mai amintiți? Cum le-ați descrie? Ce emoții ați trăit în acele clipe? Care dintre formele Naturii v-au impresionat cel mai mult?</p>
                         <p>Natura ne înconjoară, ne îmbrățișează cu brațele ei călduroase, ne împresoară plăcut oriunde am fi. Ne oferă șansa de a descoperi însăși esența vieții și calea spre evoluție. Natura ne încarcă cu energie; cu dărnicie, ne susține traiul și experiența pământeană prin hrană, aer curat, apă, habitat, păduri, deșerturi, munți, câmpii întinse, grâne, varietate și diversitate a florei și a faunei. Natura are grijă de noi, oamenii, cei care, în ultimele decenii, am început să decimăm bucăți din ce în ce mai mari din această lume, parte integrantă a echilibrului Naturii.</p>
                        <p>Printre primele cuvinte îngânate de bebeluși se numără și ”ham”, ”miau”, ”mor mor”, ”beee beee”, onomatopee rostite în Natură de diverse animăluțe. Conexiunea umană cu Natura nu poate fi întreruptă, Natura face parte din noi, încă de la naștere. În momentul în care se aleg materialele pentru o nouă gamă de hăinuțe, experții de la iELM caută îndeosebi modele inspirate de Natură, modele care să întărească această legătură unică a celor mici cu Natura, prezentă din fașă.</p>
                        <div class="blog-one-quote">
                            <p><span><i class="fa fa-angle-double-right"></i></span> Dacă e să privim lucrurile mai filosofic, noi nu suntem stăpânii planetei, și nu planeta există datorită nouă, dar noi existăm datorită planetei. Planeta este casa trupului nostru și așa cum avem grijă de casele noastre în care locuim cu familia, așa ar trebui să îngrijim și planeta, care defapt este baza existenței. </p>
                            <p><span><i class="fa fa-angle-double-right"></i></span>Dacă poluăm mediul ambiant dăunăm direct fiecărei viețuitoare în parte, care poate nu o să își dea suflarea din start, dar va degrada pe parcurs și astfel în timp ne vom isca cu o ecologie distrusă, absolut neprielnică pentru viață.</p>
                            <p><span><i class="fa fa-angle-double-right"></i></span>O simplă dispariție de o specie de animale sau plante, aparent nesemnificativă, va afecta considerent starea și calitatea mediului. </p>
                        </div>
                        <p>Cu cât ne vom implica mai puțin în bunul circuit al naturii, cu atât vom dobândi un echilibru mai sănătos și mai sigur pentru un trai decent, în comuniune cu natura.</p>
