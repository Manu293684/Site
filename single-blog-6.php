                        <h2>Peșteri în Caraș-Severin</h2>
                        <h4 class="posted">de <a class="admin" href="#">Timotei Sabo</a> / <a class="date" href="#">Iunie 12</a>, <a class="year" href="#">2020</a></h4>
                        <p>Judetul Caras Severin având un relief în mare parte carstic gazduieste un mare numar de pesteri, multe din acestea situându-se în imediata apropiere a unor trasee turistice marcate, sau chiar a unor drumuri si localitati. Astfel multe din aceste pesteri au devenit obiective turistice de mare interes, în ciuda faptului ca doar doua din ele au niste amenajari turistice minime (Pestera Comarnic si Pestera Hotilor) si doar una din ele are ghid (Pestera Comarnic).Cu câteva exceptii pesterile ce pot fi vizitate se afla în versantii sau în vecinatatea unor chei sau defilee de mare frumusete, strabatute de drumuri sau trasee turistice marcate. </p> 
                        <p>Pesterile nu trebuie vazute ca niste obiective turistice singulare, izolate, ci ca o completare ce aduce un plus de valoare si diversitate, frumusetii peisajului carstic existent.</p>
                        <p>Vizita într-o pestera implica întotdeauna o oarecare doza de risc, dar daca acesta este constientizat si sunt luate masurile de precautie necesare, vizitarea unei pesteri poate fi o experienta de neuitat, într-o lume radical diferita de cea cu care suntem obisnuiti. </p>
						
                       <p><b>Pestera Liliecilor</b> din Cheile Carasului este situata in apropierea comunei Carasova, judetul Caras-Severin. Intrarea de mari dimensiuni in Pestera Liliecilor este deja emblematica pentru Cheile Carasului, forma acesteia asemanatoare continentului african este ea insasi un obiectiv turistic.</p>
                       <p><b>Pestera de dupa Cârsa .</b>Este situata în versantul stâng al Cheilor Carasului la mai putin de 300m de podul pe care trece DJ. Se poate ajunge la aceasta urcând pe versant pâna la o dolina de prabusire în care se afla intrarea în pestera, înainte de ajunge la Pestera Valeaga sau direct prin Pestera Valeaga urcând un mic horn din finalul acesteia (varianta mai directa, dar mai dificila). Pestera are o lungime de circa 1300 m din care 800 m explorati de Asociatia Speologica Exploratorii, ultimii 500 m au fost adaugati în urma unor explorari recente. Galeria de acces este fosila, apoi se coboara pâna la un lac sifon (în mare parte a timpului sec), de unde galeria se desparte în doua: o parte continua în amonte pe o galerie îngusta si relativ înalta, urmând albia unui curs de apa temporar, care apare în perioadele bogate în precipitatii; cealalta ramura porneste de pe fundul lacului si este o galerie joasa cu strâmtori, saritori si puturi. Cea de pe urma este nerecomandata turistilor, fiind foarte periculoasa vizitarea acesteia în perioade nepotrivite (precipitatii sau iminenta unor precipitatii), sau fara echipament si experienta speologica corespunzatoare.</p>
