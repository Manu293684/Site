                        <h2>Transport</h2>
                        <h4 class="posted">de <a class="admin" href="#">Ana Olga</a> / <a class="date" href="#">Iulie 08</a>, <a class="year" href="#">2020</a></h4>
                        <p>Nu ştiu alţii cum sunt, dar eu când mă gândesc la călătorii şi mijloace de transport, nu spun nu. Aş refuza doar o variantă înjositoare de transport. Dar bicicleta, avionul, trenul, maşina personală, maşina închiriată, barca, vaporul, funicularul, telegondola, autocarul, autobuzul, microbuzul, raba, docarul, motocicleta, căruţa…toate au avut măcar o dată locul lor în viaţa mea.</p> 
                        <p>Țin minte şi-acum prima mea călătorie cu avionul, un zbor cu Wizz Air către Dortmund, în cel de-al doilea an de facultate. Destinaţia finală era Amsterdam, dar ne costa foarte puţin să zburăm până în Dortmund şi-apoi să luăm trenul până în oraşul bicicletelor, al străzilor cu miros de iarbă şi-al bulbilor de lalele. Pentru mine zborul în sine era o mare realizare, un prilej de bucurie, o experienţă memorabilă pe care o împărtăşeam cu ASER-iştii. Şi cu prietena mea, M., care era absolut terifiată pe scaunul de lângă mine. Dar eu mă bucuram de drum. Şi-n cele din urmă, m-am bucurat şi de destinaţie.</p>
                        <p>Indispensabilă. Și nu numai când vine vorba despre călătorii. Dacă am fi atenți la fiecare bănuț cheltuit, ne-am da seama că de cele mai multe ori cumpărăm o grămadă de lucruri de care pur și simplu nu avem nevoie. Îți este și ție cunoscută întrebarea: oare pe ce am dat banii? Ei bine, educația financiară se transformă în creativitatea cu care reușești să păcălești nevoile care nu sunt nici imediate, nici indispensabile. Altfel spus, momentul când reușești să nu mai cumperi din impuls lucruri de care, în realiate, nu ai nevoie, ci începi să iei decizii raționale bazate pe nevoia reală. </p>
                        <p>Cu trenul am parcurs mai ales ruta Brăila-Bucureşti-Brăila sau Bucureşti-staţiune de pe valea Prahovei-Bucureşti, dar dincolo de asta am ajuns şi pe la Cluj/Timişoara. Şi tot cu trenul am fost prima (şi singura dată :P) în Ucraina, în clasa a VI-a. Eram 5 copii de generală şi-o profă de rusă, translatorul nostru oficial la concursul internaţional de matematică distractivă, Kangaroo. Am dormit în vagonul cuşetă, iar eu am ales, desigur, locul cel mai de sus. (am poze, dar pe hârtie, într-un album bine păstrat la Brăila)</p>
                        <p>Cu căruţa m-am plimbat prin satul bunicii de nenumărate ori. Dar şi prin munţii Apuseni, într-una din tabere. Cu maşina (personală a altora, căci eu nu posed încă automobil) am străbătut de multe ori România, prin locuri cunoscute şi-altele mai puţin cunoscute; doar în Maramureş, Arad şi-n Iaşi n-am ajuns deloc. Am descoperit-o şi-am redescoperit-o de zeci de ori.</p>
