                        <h2>Turism</h2>
                        <h4 class="posted">de <a class="admin" href="#">Florin Dima</a> / <a class="date" href="#">Octombrie 04</a>, <a class="year" href="#">2020</a></h4>
                        <p>Caras-Severin este unul dintre judetele Romaniei ce ascunde o multime de comori, gata de explorat, vizitat sau de privit indelung, in razele de la amiaza.</p> 
                        <p> Am aminti aici Parcul National Portile de Fier, Clisura Dunarii, Muzeul de locomotive din Resita, Baile Herculane si multe altele.</p>
                        <p>Daca te uiti pe harta judetului Caras-Severin, vezi ca este brazdat de munti si rezervatii naturale, lacuri, orase si statiuni turistice atractive. Nu poti sa spui ca nu ai avea ce sa faci intr-un weekend in aceasta regiune; din contra, daca analizezi toate posibilitatile, s-ar putea sa fie nevoie de mai multe weekend-uri petrecute in Caras-Severin.</p>
                        <h2>Iata, pe scurt, cam ce s-ar putea face in judetul Caras-Severin:</h2>
						<p><b>O vizita la Manastirea Piatra Scrisa. </b>Probabil ca nu ai auzit prea multe despre aceasta manastire, desi este situata la numai 25 km de orasul Caransebes. Daca faci o plimbare spre Orsova sau o excursie pe Dunare, vei trece pe langa ea – manastirea se afla in mijlocul unui decor incantator, in Valea Timisului, traversata in vremuri de demult de fostul drum imperial care lega doua regiuni: Oltenia si Banatul. Manastirea Piatra Scrisa a fost mentionata documentar pentru prima data intr-o harta militara austriaca din 1788. Interesant este ca in interiorul lacasului de cult este o icoana zugravita pe stanca, ce reprezinta Sfanta Treime; a fost descoperita in timpul unor lucrari de constructie la calea ferata care avea sa lege orasele Caransebes si Orsova. Ca sa protejeze icoana, constructorii au deviat lucrarile tunelului spre Armenis. Se spune ca Icoana de la Piatra Scrisa este facatoare de minuni, si in fiecare an aduna in jurul ei numerosi credinciosi.</p>
                        <p><b>O zi relaxanta in Poiana Marului. </b>Daca alegi sa te cazezi la pensiunile din Caransebes, atunci ai putea sa-ti petreci o zi din weekend in statiunea Poiana Marului, reprezentativa pentru intregul Banat, aflata la 45 km de Caransebes. La Poiana Marului te vei bucura de o zi relaxanta in mijlocul naturii, intr-un loc de o frumusete rara. Inconjurata de Muntii Tarcului, bogati in paduri de foioase si conifere, statiunea este o oaza de prospetime, cu aer curat si peisaje de poveste. Plimba-te pe dealuri si in zavoaie, odihneste-te pe malul Lacului Poiana Marului si stai la taifas cu localnicii. Ai sa vezi ce mandri sunt de regiunea lor si cum unii dintre ei iti vor povesti ca acolo ar fi ingropat regele dac Scorilo, tatal lui Decebal. Altii spun ca in zona ar fi ingropata comoara lui Scorillo, mai “exact” in adancurile Stancii lui Scorilo din masivul Muntele Mic. Nu stim daca ai s-o descoperi, dar cu siguranta o drumetie pe cararile montane iti va imbogati albumul cu fotografii. Cum zona este plina de pensiuni, poti sa te odihnesti la una dintre ele si sa te bucuri de o masa sanatoasa, cu mancaruri delicioase din Banat.</p>
                        <p><b>O zi de schi in Semenic. </b>Pentru ca iarna bate la usa si vremea se poate schimba de la o zi la alta, iti mai dam o idee de cum ti-ai putea petrece weekendul in judetul Caras-Severin: la schi. Exista doua zone importante in acest judet unde poti schia, in Muntii Semenic si pe Muntele Mic. Cele cinci partii de schi din Semenic, aflate la o altitudine de 1400 de metri, sunt asaltate (si apreciate) in fiecare an de turisti, incepatori sau profesionisti. Du-te pe partia Casa Baraj-Valiug daca esti incepator, si pe celelalte (Slalom Urias, Goznuta, Semenic-Valiug si Crucea de Brazi) daca esti profesionist. Toate sunt dotate cu instalatie de transport prin cablu, iar de zapada naturala nu vei duce lipsa atat timp cat tine iarna! Si pe Muntele Mic exista trei partii de schi amenajate (desi nu au nocturna): Partia Sub Telescaun, Valea Soarelui si Partia Nordica. Mai mult, in jurul lor se organizeaza in fiecare an Carnavalul Zapezii (in timpul vacantei de iarna) si Cupa Muntele Mic (la inceputul lunii septembrie).</p>
                        <p><b>O vizita la Pestera cu Oase. </b>Aproape de orasul Anina (in imprejurimile caruia te poti plimba cu mocanita) este Pestera cu Oase, o grota uriasa care adaposteste 12 galerii interioare. Trebuie sa stii insa ca nu poti inainta in aceasta pestera fara echipament special de explorare. Obiectivul este foarte important din punct de vedere antropologic, pentru ca in maruntaiele acestei pesteri s-au descoperit cele mai vechi ramasite umane din Europa. S-a descoperit ca fosilele gasite la Anina aveau o vechime de peste 35.000 de ani – stiai, de exemplu, ca aici a fost descoperita cea mai veche mandibula a unui homo sapiens din Europa, despre care se spune ca ar avea peste 40.000 de ani?</p>
                        <p><b>O zi la morile Rudariei. </b>Daca vrei sa vezi un loc unic in tot sud-estul Europei, du-te pe Valea Rudariei, aproape de comuna Eftimie Murgu. Zona de la poalele muntilor Almajului este fermecatoare si iti ofera ocazia sa vezi cum se traia odinioara. Da, in aceste timpuri moderne, mai ai inca ocazia sa vezi o farama de istorie. Vestea celor 22 de mori de apa cu ciutura de la Rudaria, satul atestat la 1470, a ajuns pana in strainatate. Fascinant este insa faptul ca morile functioneaza si astazi la fel de bine, iar locuitorii le folosesc ca sa-si macine cerealele, dupa metoda mostenita din batrani. Locul este foarte pitoresc, iar morile – pur si simplu fascinante!</p>
